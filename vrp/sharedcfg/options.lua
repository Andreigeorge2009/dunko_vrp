vRPConfig = {} -- Global variable for easy referencing. 

vRPConfig.MoneyUiEnabled = true; -- Set to false to disable Money in the top right corner. 
vRPConfig.SurvivalUiEnabled = true; -- Controls the UI under the healthbar.
vRPConfig.EnableComa = true; -- Controls the vRP coma on death.
vRPConfig.EnableFoodAndWater = true; -- Controls the food and water system.
vRPConfig.EnableHealthRegen = true; -- Controls the health regen. (Whether they regen health after taking damage do not disable if coma is enabled.)
vRPConfig.EnableBuyVehicles = true; -- Enables ability to buy vehicles from the RageUI Garages.  
vRPConfig.LoadPreviews = true; -- Controls the car previews with the RageUI Garages.
vRPConfig.VehicleStoreRadius = 250; -- Controls radius a vehicle can be stored from.
vRPConfig.AdminCoolDown = false; -- Enables an admin cooldown 
vRPConfig.AdminCooldownTime = 60; -- 1 minute in (seconds) duration of cooldown. 
vRPConfig.StoreWeaponsOnDeath = false; -- Stores the players weapon on death allowing them to be looted.
vRPConfig.DoNotDisplayIps = false; -- Removes all vRP related references in the console to player ip addresses.